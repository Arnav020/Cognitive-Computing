def swap_list_elements(lst, pos1, pos2):
    temp = lst[pos1]
    lst[pos1] = lst[pos2]
    lst[pos2] = temp
    return lst

lst = [1, 2, 3, 4, 5]
print("Swapped List:", swap_list_elements(lst, 1, 3))

