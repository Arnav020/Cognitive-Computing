def swap_set_elements(s, pos1, pos2):
    s_list = list(s)
    s_list[pos1], s_list[pos2] = s_list[pos2], s_list[pos1]
    return set(s_list)

s = {10, 20, 30, 40, 50}
print("Swapped Set:", swap_set_elements(s, 0, 3))

